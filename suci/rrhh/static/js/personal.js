function MostrarEdades1() {
    let hijos = document.getElementById('hijos').value;
    if (hijos == "true") {
        div = document.getElementById('edades1');
        div.style.display = 'block';
    } else {
        div = document.getElementById('edades1');
        div.style.display = 'none';
    }
}
